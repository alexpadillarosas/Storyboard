import UIKit


class Contact {
    let photo: String
    let name: String
    let position: String
    let email: String
    let phone: String
    
    init(photo: String, name: String, position: String, email: String, phone: String) {
        self.photo = photo
        self.name = name
        self.position = position
        self.email = email
        self.phone = phone
    }
    
}
